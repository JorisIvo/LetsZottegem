<h2 class="titel">Lid worden</h2>
<p>Beste LETS-ge&iuml;nteresseerde,</p>
Lid worden kan nadat je een info-avond = infosessie (ongeveer 2 uurtjes), meevolgde.<br />
We organiseren die ongeveer om de 2 maanden (afhankelijk van de interesse),<br />
ze zullen op deze website verschijnen in de kalender'<!-- <a href="kalender.html">kalender</a> -->'.<br
/>
<br />
We vinden dit echt noodzakelijk omdat we dan<br />
het LETS-systeem, onze streekeigen werking, <br />
en de werking met onze ELAS (beveiligde website waarop we alles kunnen vinden en regelen) <br />
uitleggen, en dat we jullie vragen daarover beantwoorden<br />
&hellip;<br />
zodat jullie daarna kunnen beslissen of jullie graag onze groep versterken als lid.<br />
Dit kan op de avond zelf als je graag snel begint, of later &hellip; ;-)<br />
(Het volgen van een infosessie houdt dus geen verplichting in,<br />
het aanvragen van info (onderaan) dus zeker niet.)<br />
<br />
Met een beetje geluk leer je direct ook nieuwe letsers kennen die avond.<br />
<br />
In stapjes:<br />
- je laat ons weten dat je <a href="/contact">interesse</a> hebt<br />
- je krijgt alvast wat info<br />
- je wordt uitgenodigd (tijdstip en adres) en volgt een infosessie<br />
- als je lid wil worden vul je een infoblad in en schrijf je het lidgeld over<br />
(&euro;15/jaar voor lidgeld+verzekering voor werkenden,<br />
aangepaste tarieven voor wie (tijdelijk) geen werk heeft, meer info krijg je op de infosessie)<br />
- je krijgt een lidnr. en account voor ELAS<br />
- je krijgt ook 100 IETSEN om mee te starten <br />
<br />
Vriendelijke groeten, en hopelijk tot ziens &hellip; of zelfs tot LETS !<br />
