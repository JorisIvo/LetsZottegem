<ul><li><h2><a name="actief_letser"></a><a rel="noreferrer">Hoe word ik een actief Letser?</a></h2><br>
<ul dir="ltr"><li><b>Lezen, en aan LETS denken ;-)</b><br>
<br>
Als lid van onze letsgroep kan je inloggen op ELAS, onze LETSsite.<br>
In ELAS vind je de vragen en aanbiedingen van de andere letsers en hun
contactgegevens. <br>
<br>
Lees regelmatig wat anderen vragen en aanbieden en reageer op dingen die jou interesseren.
Wacht niet tot anderen zelf contact opnemen met jou. De ervaring leert
dat de nieuwkomers met de meeste zin voor initiatief snel actieve nieuwe Letsers worden.<br>
<br>
Laat je niet afschrikken door eens een teleurstellende Letsuitwisseling. Met sommige Letsers zal het onmiddellijk klikken, met
anderen niet.<br>
<br>
</li><li><b>Letsen</b><br>
<br>Aarzel niet om eerst al ietsen uit te geven en er dan pas te
verdienen (Je kreeg er toch 100 om mee te starten, en je mag onder nul
gaan). Belangrijk is dat je begint te Letsen en wat Letsers leert
kennen. Zij kunnen je ook tips geven over wie welke dienst vraagt of
aanbiedt.
En zodra je wat mensen kent, is de bal aan het rollen.<br>
<br>
</li><li><b>Activiteiten</b><br>
<br>
De beste manier om in contact te komen met andere Letsers, is simpelweg
door te Letsen of deel te nemen aan &egrave;&egrave;n of andere Lets-activiteit. De
Lets-activiteiten worden aangekondigd via mail en bij het nieuws in ELAS. </li></ul><a rel="nofollow noreferrer" name="1245735771_slechteexcuses"></a><br>
<br>
</li><li><h2><a name="excuses"></a><a rel="noreferrer">5 (slechte) excuses om niet te Letsen</a></h2><ul><li><b>Ik heb geen tijd</b><br>
<br>
"Mijn appelboom doet het goed. Het teveel gaat naar de liefhebbers in de
Letsgroep. Dat vraagt mij geen extra tijd. Een Letser kwam helpen op het
verjaardagsfeestje van m'n dochtertje.Even een adempauze voor mezelf
tijdens zo'n drukke namiddag"<br>
<br>
"Gisteren kreeg ik Maartens computer terug in gang. Nu kan hij verder
werken aan zijn eindwerk.&nbsp; De ietsen die ik verdiende geef ik
zaterdag aan Tim. Hij komt dan eens helpen in de tuin zodat het werk
veel vlugger klaar is."<br>
<br>
In het begin lijkt Letsen tijd te vragen. Je weet nog niet precies wat
je kan vragen, hoe je Lets moet integreren in je dagdagelijkse
bezigheden. Na een tijdje komt dit vanzelf goed. Je denkt er niet meer
over na, het moeten gaat eraf. Voor je het weet, ben je aan het
genieten.<br>
<br>
</li><li><b>Ik heb niets te bieden!</b><br>
<br>
"Ik dacht dat ik niets te bieden had tot ik in contact kwam met een
gezin Letsers uit m'n buurt. Nu giet ik hun planten als ze met verlof
zijn. Ik laat ook regelmatig hun hond uit."<br>
<br>
Het zit 'm in de simpele dingen. Je hoeft niet professioneel te zijn.
Denk aan je hobby's en je talenten ... wat doe je graag? Je kan ook
materiaal uitlenen Je hoeft er
dus niet over te piekeren, je aanbod groeit vanzelf.<br>
<br>
</li><li><b>Mijn aanbod interesseert niemand</b><br>
<br>
"Bob geeft dansles. Hij danst als de beste. Maar blijkbaar heeft niemand in de groep nu tijd en zin in dansles."<br>
<br>
Haal je aanbod niet weg omdat niemand erop in gaat. Wat vandaag niet
past, past misschien morgen wel. Of er komt een nieuw lid bij dat gek is
op dansen&#8230; Intussen kan je creatief zijn. Letsers uitnodigen om te komen
eten bij jou thuis, een dvd-avondje organiseren, gewoon ingaan op de vragen van anderen &#8230;<br>
<br>
</li><li><b>Ik heb eigenlijk niets nodig</b><br>
<br>
Toegegeven, dat siert je. Maar misschien heb je nooit geleerd jezelf
eens te verwennen. Een lekkere voetmassage of een georganiseerde
wandeltocht doet iedereen deugd.<br>
<br>
En soms dacht je er nog niet aan ... Ik besefte niet dat ik heel blij zou zijn als ik een
fietsenrek zou kunnen gebruiken. Tot ik las dat Bob een
fietsenrek uitleent. Nu hoef ik mij dat niet te kopen.<br>
<br>
</li><li><b>Wat als ik teveel in min sta?</b><br>
<br>
"Anja had een arm gebroken. Ze kreeg hulp via Lets en kon
weinig terugdoen in die periode. Ze ging behoorlijk onder nul. En wat
dan nog? Eenmaal die arm weer beter is, haalt ze dat wel in."<br>
<br>
Je moet juist weten dat het systeem stopt op min 500 ... en dat je later veel moet terugdoen natuurlijk.</li></ul><a rel="nofollow noreferrer" name="1245735771_Letsersmetweinigtijd"></a><br>
<br>
</li><li><h2><a name="weinig_tijd"></a><a rel="noreferrer">Tips voor Letsers met weinig tijd</a></h2><ul><li>Laat je wasmachine, computer, videocamera, hogedrukreiniger,&#8230;<br>
gebruiken door andere Letsers.<br>
<br>
</li><li>Leen je behangtafel, stellingen, cd's, boeken,&#8230; uit.<br>
<br>
</li><li>Als je voor jezelf kookt, maak dan een extra portie klaar en nodig een andere Letser uit.<br>
<br>
</li><li>Als je kinderen hebt, bied dan kinderopvang aan. <br>
<br>
</li><li>Hou je van dieren? Dan kan je op het huisdier van een andere Letser passen wanneer die op reis gaat.<br>
<br>
</li><li>Heb je een abonnement op een tijdschrift? Misschien hebben anderen hier ook wel interesse voor.<br>
<br>
</li><li>Groenten uit je eigen tuin, eieren van je kippen, zelfgebakken brood, plantenstekjes,&#8230; Je kan er allemaal ietsen voor krijgen.<br>
<br>
</li><li>Sommige Letsers hebben geen auto. Stel voor ge&iuml;nteresseerden mee te
nemen als je boodschappen gaat doen. En waarom niet je medeLetsers
verwittigen als je op uitstap of reis gaat? Misschien hebben zij wel
ietsen over voor een lift naar een leuke bestemming.<br>
<br>
</li><li>Alle ietsen die je aan een andere Letser geeft, is een engagement om
vroeg of laat ook iets voor een ander te doen. Vraag dus niet meer dan
je kan geven.</li></ul></li></ul>

